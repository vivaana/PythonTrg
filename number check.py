yourNumber = int(input("input please enter a number \n"))
if yourNumber >= 10 and yourNumber < 100:
    print("high")

elif yourNumber >= 100 and yourNumber < 500:
    print("higher")

elif yourNumber >= 500 and yourNumber < 1000:
    print("big")

elif yourNumber >= 100:
    print("massive")

elif yourNumber <= 10:
    print("low")




